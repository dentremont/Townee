	<h1>About Town.ee</h1>
	
	


<p>Use it to see what people in your town are up to. We like to use it to see what lunch specials there are, what bands are playing, and to get a quick sense of what's going on in our own town. Other people could use it to:</p>

<ul>
    <li>Find Jobs</li>
    <li>Find Places they can take their kids</li>
    <li>Get good date ideas</li>
    <li>See where the party's at!</li>
    <li>And countless other things</li>
</ul>


<h1>What's special about Town.ee?</h1>

<p>Our vision for Town.ee is that it becomes a town bulletin board. Similar to something you might find at a public library or a downtown light post. A place where citizens can see what's going on in their town and be a part of it. Our goal is to create better communities by building a platform that allows people to engage in it. Often people want to be involved they are just uninformed. We hope townee changes that.</p>


