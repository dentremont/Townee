<h1>How to participate</h1>


<h2>Posting is easy</h2>

<p>If your using twitter just include one of the registered hash tags in your tweet. </p>

<p>If your twitter profile or the geo location of your tweet is located in one of the participating towns then your tweet gets posted for that town to see! </p>


<h4>Example</h4> 

<p>I'm at Joe's BBQ and the rib plate is half off! #lunchspecial #goodeats.</p>

<p>This tweet will then be posted to both the Lunch Special and Good Eats town boards in your town for the community to view. </p>



<h2>Adding town tags.</h2> 

<p>Sign up and you can add new town tags to for your town to post too.</p> 

<P>Once you are logged in, go to my dashboard. Scroll down and you will see the form to register new hash tags.</p> 

<p><em>Note: You will also become the moderator and can delete any spam that may creep up on boards that you create.</em></p> 


<h2>Following your favorite Town Tags.</h2> 

<p>Sign up and you can save your favorite town tags to your dashboard.</p> 

<p>Once you are logged in, go to my dashboard. Scroll down and you will see a list of town tags. Click the ones you like to add to your dashboard and save your settings. </p>


<h2>FAQs</h2>

<p><strong>Q: Why aren't my tweets posting to town.ee?</strong></p>

<p>A: Protected tweets will not show up on Townee. Also, make sure that the location settings are set to a participating town. </p>

<p>Still can't get it to work?</p> 

<p>Then click the feedback button on the side of the site and let us know.</p>   


<p>Q: Can you add my town?</p>

<p><strong>A: We have added all of the towns that we think have enough twitter users to support a site like this. However, if you think your town has been over looked let us know using the feedback button on the side of the site.</strong></p>



	
